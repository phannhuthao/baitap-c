int main () {
    int canh1, canh2, canh3, tong;
    printf("nhap tong cua 2 canh: ");
    scanf("%d", &canh1);
    scanf("%d", &canh2);
    printf("nhap canh3: ");
    tong = canh1 = canh2;
    if (tong > canh3) {
        printf("tong 2 canh lon hon la tam giac thuong");
    } else (tong < canh3);
    printf("tong 2 canh nho hon khong la tam giac thuong");
    return 0;
}

